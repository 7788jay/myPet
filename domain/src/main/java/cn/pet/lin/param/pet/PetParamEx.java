public class PetParamEx extends PetParam {
    private String startPrice;
    private String endPrice;
    private String startLife;
    private String endLife;

    public String getStartPrice() {
        return startPrice;
    }

    public void setStartPrice(String startPrice) {
        this.startPrice = startPrice;
    }

    public String getEndPrice() {
        return endPrice;
    }

    public void setEndPrice(String endPrice) {
        this.endPrice = endPrice;
    }

    public String getStartLife() {
        return startLife;
    }

    public void setStartLife(String startLife) {
        this.startLife = startLife;
    }

    public String getEndLife() {
        return endLife;
    }

    public void setEndLife(String endLife) {
        this.endLife = endLife;
    }
}
